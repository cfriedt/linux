#ifndef MCP9808_REGS_H_
#define MCP9808_REGS_H_

enum {
	MCP9808_RFU,    // RFU, Reserved for Future Use (Read-Only register)
	MCP9808_CONFIG, // Configuration register (CONFIG)
	MCP9808_TUPPER, // Alert Temperature Upper Boundary Trip register (TUPPER)
	MCP9808_TLOWER, // Alert Temperature Lower Boundary Trip register (TLOWER)
	MCP9808_TCRIT,  // Critical Temperature Trip register (TCRIT)
	MCP9808_TA,     // Temperature register (TA)
	MCP9808_MID,    // Manufacturer ID register (MID)
	MCP9808_DID,    // Device ID/Revision register (DID)
	MCP9808_RES,    // Resolution register (RES)
	MCP9808_NREG_,
	// all other registers are reserved (Some registers contain calibration codes and should not be accessed.)
};

#define MCP9808_RFU_SHIFT        0
#define MCP9808_RFU_MASK         ( 0x1 << MCP9808_CONFIG_RFU_SHIFT )
#define MCP9808_RFU_SIZE_BYTES   2
#define MCP9808_RFU_DEFAULT      0x001f
#define MCP9808_RFU_WRITE_MASK   0x0000
#define MCP9808_RFU_READ_MASK    0xffff

#define MCP9808_CONFIG_ALERT_MOD_SHIFT  0
#define MCP9808_CONFIG_ALERT_MOD_MASK   ( 0x1 << MCP9808_CONFIG_ALERT_MOD_SHIFT )
#define MCP9808_CONFIG_ALERT_POL_SHIFT  1
#define MCP9808_CONFIG_ALERT_POL_MASK   ( 0x1 << MCP9808_CONFIG_ALERT_POL_SHIFT )
#define MCP9808_CONFIG_ALERT_SEL_SHIFT  2
#define MCP9808_CONFIG_ALERT_SEL_MASK   ( 0x1 << MCP9808_CONFIG_ALERT_SEL_SHIFT )
#define MCP9808_CONFIG_ALERT_CNT_SHIFT  3
#define MCP9808_CONFIG_ALERT_CNT_MASK   ( 0x1 << MCP9808_CONFIG_ALERT_CNT_SHIFT )
#define MCP9808_CONFIG_ALERT_STAT_SHIFT 4
#define MCP9808_CONFIG_ALERT_STAT_MASK  ( 0x1 << MCP9808_CONFIG_ALERT_STAT_SHIFT )
#define MCP9808_CONFIG_INT_CLEAR_SHIFT  5
#define MCP9808_CONFIG_INT_CLEAR_MASK   ( 0x1 << MCP9808_CONFIG_INT_CLEAR_SHIFT )
#define MCP9808_CONFIG_WIN_LOCK_SHIFT   6
#define MCP9808_CONFIG_WIN_LOCK_MASK    ( 0x1 << MCP9808_CONFIG_WIN_LOCK_SHIFT )
#define MCP9808_CONFIG_CRIT_LOCK_SHIFT  7
#define MCP9808_CONFIG_CRIT_LOCK_MASK   ( 0x1 << MCP9808_CONFIG_CRIT_LOCK_SHIFT )
#define MCP9808_CONFIG_SHDN_SHIFT       8
#define MCP9808_CONFIG_SHDN_MASK        ( 0x1 << MCP9808_CONFIG_SHDN_SHIFT )
#define MCP9808_CONFIG_THYST_SHIFT      9
#define MCP9808_CONFIG_THYST_MASK       ( 0x3 << MCP9808_CONFIG_THYST_SHIFT )
#define MCP9808_CONFIG_SIZE_BYTES       2
#define MCP9808_CONFIG_DEFAULT          0
#define MCP9808_CONFIG_WRITE_MASK       0x07ef
#define MCP9808_CONFIG_READ_MASK        0x07df

#define MCP9808_TEMP_FRACTIONAL_SHIFT   0
#define MCP9808_TEMP_FRACTIONAL_MASK    ( 0xf << MCP9808_TEMP_FRACTIONAL_SHIFT )
#define MCP9808_TEMP_INTEGRAL_SHIFT     4
#define MCP9808_TEMP_INTEGRAL_MASK      ( 0xff << MCP9808_TEMP_INTEGRAL_SHIFT )
#define MCP9808_TEMP_SIGN_SHIFT         12
#define MCP9808_TEMP_SIGN_MASK          ( 0x1 << MCP9808_TEMP_SIGN_SHIFT )
#define MCP9808_TEMP_TA_LT_TLOWER_SHIFT 13
#define MCP9808_TEMP_TA_LT_TLOWER_MASK  ( 0x1 << MCP9808_TEMP_TA_LT_TLOWER_SHIFT )
#define MCP9808_TEMP_TA_GT_TUPPER_SHIFT 14
#define MCP9808_TEMP_TA_LT_TUPPER_MASK  ( 0x1 << MCP9808_TEMP_TA_GT_TUPPER_SHIFT )
#define MCP9808_TEMP_TA_GE_TCRIT_SHIFT  15
#define MCP9808_TEMP_TA_GE_TCRIT_MASK   ( 0x1 << MCP9808_TEMP_TA_GE_TCRIT_SHIFT )
#define MCP9808_TEMP_DEFAULT            0

#define MCP9808_TUPPER_SIZE_BYTES 2
#define MCP9808_TUPPER_DEFAULT    MCP9808_TEMP_DEFAULT
#define MCP9808_TUPPER_WRITE_MASK 0x1ff6
#define MCP9808_TUPPER_READ_MASK  0x1ffc

#define MCP9808_TLOWER_SIZE_BYTES 2
#define MCP9808_TLOWER_DEFAULT    MCP9808_TEMP_DEFAULT
#define MCP9808_TLOWER_WRITE_MASK 0x1ff6
#define MCP9808_TLOWER_READ_MASK  0x1ffc

#define MCP9808_TCRIT_SIZE_BYTES  2
#define MCP9808_TCRIT_DEFAULT     MCP9808_TEMP_DEFAULT
#define MCP9808_TCRIT_WRITE_MASK  0x1ff6
#define MCP9808_TCRIT_READ_MASK   0x1ffc

#define MCP9808_TA_SIZE_BYTES 2
#define MCP9808_TA_WRITE_MASK 0x0000
#define MCP9808_TA_DEFAULT    MCP9808_TEMP_DEFAULT
#define MCP9808_TA_READ_MASK  0xffff

#define MCP9808_MID_SHIFT      0
#define MCP9808_MID_MASK       ( 0xffff << MCP9808_MID_SHIFT )
#define MCP9808_MID_SIZE_BYTES 2
#define MCP9808_MID_DEFAULT    0x0054
#define MCP9808_MID_WRITE_MASK 0x0000
#define MCP9808_MID_READ_MASK  0xffff

#define MCP9808_DID_REVISION_SHIFT   0
#define MCP9808_DID_REVISION_MASK    ( 0xff << MCP9808_DID_REVISION_SHIFT )
#define MCP9808_DID_REVISION_DEFAULT 0
#define MCP9808_DID_DEVICE_SHIFT     8
#define MCP9808_DID_DEVICE_MASK      ( 0xff << MCP9808_DID_DEVICE_SHIFT )
#define MCP9808_DID_DEVICE_DEFAULT   4
#define MCP9808_DID_SIZE_BYTES       2
#define MCP9808_DID_DEFAULT \
	( \
		0 \
		| ( ( MCP9808_DID_REVISION_MASK & MCP9808_DID_REVISION_DEFAULT ) << MCP9808_DID_REVISION_SHIFT ) \
		| ( ( MCP9808_DID_DEVICE_MASK   & MCP9808_DID_DEVICE_DEFAULT   ) << MCP9808_DID_DEVICE_SHIFT   ) \
	)
#define MCP9808_DID_WRITE_MASK  0x0000
#define MCP9808_DID_READ_MASK   0xffff

#define MCP9808_RES_SHIFT      0
#define MCP9808_RES_MASK       ( 0x3 << MCP9808_RES_SHIFT )
#define MCP9808_RES_SIZE_BYTES 1
#define MCP9808_RES_DEFAULT    0x0003
#define MCP9808_RES_WRITE_MASK 0x0003
#define MCP9808_RES_READ_MASK  0x0003

#endif /* MCP9808_REGS_H_ */
